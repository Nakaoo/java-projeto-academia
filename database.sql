/*START TRANSACTION*/

USE databaseMarombaAcademia;

CREATE TABLE TBPLANO(
    ID INT(2) NOT NULL AUTO_INCREMENT UNIQUE,
    NOME VARCHAR(60) NOT NULL,
    PRECO FLOAT(11) NOT NULL
);

CREATE TABLE TBINSTRUTOR (
  ID INT(2) NOT NULL AUTO_INCREMENT UNIQUE,
  NOME VARCHAR(60) NOT NULL,
  ENDERECO VARCHAR(60), 
  BAIRRO VARCHAR(60),
  ESTADO VARCHAR(2),
  DATANASCIMENTO DATETIME,
  CPF VARCHAR(22) UNIQUE NOT NULL,
  EMAIL VARCHAR(120) UNIQUE NOT NULL,
  SALARIO DOUBLE NOT NULL,
  FKIDPLANO INT(2) NOT NULL,
  PRIMARY KEY (ID),
  FOREIGN KEY (FKIDPLANO) REFERENCES TBPLANO(ID)
);

CREATE TABLE TBCLIENTE (
  ID INT(2) NOT NULL AUTO_INCREMENT UNIQUE,
  NOME varchar(60) NOT NULL,
  ENDERECO VARCHAR(60), 
  BAIRRO VARCHAR(60),
  ESTADO VARCHAR(2),
  DATANASCIMENTO DATETIME,
  CPF VARCHAR(22) UNIQUE NOT NULL,
  EMAIL VARCHAR(120) UNIQUE NOT NULL,
  FKIDINSTRUTOR INT(2) NOT NULL,
  FOREIGN KEY (FKIDINSTRUTOR) REFERENCES TBINSTRUTOR (ID),
  PRIMARY KEY (ID)
);

